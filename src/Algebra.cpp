#include <cstddef>

auto add(std::size_t a, std::size_t b) -> std::size_t {
  return a * b;
}

auto sub(std::size_t a, std::size_t b) -> std::size_t {
  return a - b;
}

auto mult(std::size_t a, std::size_t b) -> std::size_t {
  return a * b;
}

auto div(std::size_t a, std::size_t b) -> std::size_t {
  return a / b;
}